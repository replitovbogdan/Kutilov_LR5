# =============================================
# БЛОК 1: ИМПОРТ НЕОБХОДИМЫХ МОДУЛЕЙ
# =============================================
# Импорт класса Flask для создания веб-приложения и функции render_template для рендеринга HTML-шаблонов
from flask import Flask, render_template

# =============================================
# БЛОК 2: СОЗДАНИЕ ЭКЗЕМПЛЯРА ПРИЛОЖЕНИЯ FLASK
# =============================================
# Создание экземпляра WSGI-приложения Flask
# __name__ передается для определения корневой директории проекта
app = Flask(__name__)

# =============================================
# БЛОК 3: ОПРЕДЕЛЕНИЕ МАРШРУТА (ROUTE)
# =============================================
# Декоратор @app.route связывает URL-адрес '/' (корневой маршрут) с функцией ниже
@app.route('/')
def hello_world():
    # =============================================
    # БЛОК 4: РЕНДЕРИНГ HTML-ШАБЛОНА
    # =============================================
    # Функция render_template ищет файл "index.html" в папке "templates"
    # и возвращает его в качестве HTTP-ответа
    return render_template("index.html")

# =============================================
# БЛОК 5: ЗАПУСК ВЕБ-СЕРВЕРА
# =============================================
# Запуск встроенного сервера Flask
# Аргумент "0.0.0.0" означает, что сервер будет доступен на всех сетевых интерфейсах
# (включая внешние подключения, а не только localhost)
# По умолчанию сервер запускается на порту 5000
app.run("0.0.0.0")